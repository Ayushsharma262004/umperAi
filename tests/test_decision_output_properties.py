@settings(suppress_health_check=[HealthCheck.too_slow])
@given(
    decisions=st.lists(decision_strategy(), min_size=2, max_size=5)
)
def test_property_dismissal_always_highest_priority(decisions):
    """
    Test that dismissal events always have the highest priority among any set of decisions.
    """
    output_handler = DecisionOutput()
    
    # Get priorities for all decisions
    priorities = [output_handler.get_decision_priority(d) for d in decisions]
    
    # Find dismissal decisions
    dismissal_indices = [
        i for i, d in enumerate(decisions)
        if d.event_type in (EventType.BOWLED, EventType.CAUGHT, EventType.LBW)
    ]
    
    # If there are dismissal decisions, they should all have HIGH priority
    for idx in dismissal_indices:
        assert priorities[idx] == DecisionPriority.HIGH, (
            f"Dismissal event {decisions[idx].event_type} should have HIGH priority"
        )
    
    # If there are dismissal decisions, no other decision should have higher priority
    if dismissal_indices:
        dismissal_priority_value = DecisionPriority.HIGH.value
        for i, priority in enumerate(priorities):
            if i not in dismissal_indices:
                assert priority.value <= dismissal_priority_value, (
                    f"Non-dismissal event {decisions[i].event_type} has priority {priority.value} "
                    f"which is not less than or equal to dismissal priority {dismissal_priority_value}"
                )


