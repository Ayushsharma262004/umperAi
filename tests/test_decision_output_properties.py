# Property 21: Decision Output Completeness
# ============================================================================

@given(decision=decision_strategy())
def test_property_21_decision_output_completeness(decision):
    """
    Property 21: Decision Output Completeness
    
    For any decision generated by the Decision Engine, the Decision_Output
    SHALL include the event type, confidence level, and timestamp.
    
    **Validates: Requirements 10.2, 10.4, 11.3**
    """
    # Create output handler with text-only output (no audio/visual dependencies)
    config = OutputConfig(enable_text=True, enable_audio=False, enable_visual=False)
    output_handler = DecisionOutput(config=config)
    
    # Display decision (without frame, will print to console)
    output_handler.display_decision(decision, frame=None)
    
    # Verify that latency was measured (indicates output was generated)
    latency_stats = output_handler.get_latency_stats()
    assert latency_stats["count"] > 0, "No output was generated"
    
    # Verify latency measurement includes timestamp information
    assert latency_stats["latest_ms"] >= 0.0, "Latency measurement invalid"
    
    # Verify decision contains required fields
    assert decision.event_type is not None, "Decision missing event_type"
    assert decision.confidence is not None, "Decision missing confidence"
    assert decision.timestamp is not None, "Decision missing timestamp"
    
    # Verify event type is valid
    assert isinstance(decision.event_type, EventType), "event_type is not EventType enum"
    
    # Verify confidence is in valid range
    assert 0.0 <= decision.confidence <= 1.0, f"Confidence {decision.confidence} out of range [0.0, 1.0]"
    
    # Verify timestamp is valid
    assert decision.timestamp > 0.0, "Timestamp must be positive"
    
    # Verify formatted text includes event type
    formatted_text = output_handler._format_decision_text(decision, latency_stats["latest_ms"])
    event_name = decision.event_type.value.upper().replace("_", " ")
    assert event_name in formatted_text, f"Event type '{event_name}' not in output text: {formatted_text}"
    
    # Verify formatted text includes confidence
    confidence_pct = decision.confidence * 100
    assert f"{confidence_pct:.1f}%" in formatted_text, f"Confidence not in output text: {formatted_text}"
    
    # Verify formatted text includes latency (which is derived from timestamp)
    assert "Latency:" in formatted_text, f"Latency not in output text: {formatted_text}"


