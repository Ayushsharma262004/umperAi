@given(
    output_format=st.sampled_from(list(OutputFormat))
)
def test_property_output_format_configuration(output_format):
    """
    Test that output format can be configured and retrieved correctly.
    """
    output_handler = DecisionOutput()
    
    # Set output format
    output_handler.set_output_format(output_format)
    
    # Get output format
    retrieved_format = output_handler.get_output_format()
    
    # For specific formats, verify the configuration
    if output_format == OutputFormat.TEXT:
        assert output_handler.config.enable_text is True
        assert output_handler.config.enable_audio is False
        assert output_handler.config.enable_visual is False
        assert retrieved_format == OutputFormat.TEXT
    elif output_format == OutputFormat.AUDIO:
        assert output_handler.config.enable_text is False
        assert output_handler.config.enable_audio is True
        assert output_handler.config.enable_visual is False
        assert retrieved_format == OutputFormat.AUDIO
    elif output_format == OutputFormat.VISUAL:
        assert output_handler.config.enable_text is False
        assert output_handler.config.enable_audio is False
        assert output_handler.config.enable_visual is True
        assert retrieved_format == OutputFormat.VISUAL
    elif output_format == OutputFormat.ALL:
        assert output_handler.config.enable_text is True
        assert output_handler.config.enable_audio is True
        assert output_handler.config.enable_visual is True
        assert retrieved_format == OutputFormat.ALL
