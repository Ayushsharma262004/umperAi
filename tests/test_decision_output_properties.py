# Property 22: Decision Priority Ordering
# ============================================================================

@given(
    dismissal_event=st.sampled_from([EventType.BOWLED, EventType.CAUGHT, EventType.LBW]),
    other_event=st.sampled_from([EventType.WIDE, EventType.NO_BALL, EventType.LEGAL])
)
def test_property_22_decision_priority_ordering(dismissal_event, other_event):
    """
    Property 22: Decision Priority Ordering
    
    For any set of simultaneous match events that includes a Dismissal_Event,
    the system SHALL prioritize the Dismissal_Event output over other event types.
    
    **Validates: Requirements 10.5**
    """
    # Create output handler
    config = OutputConfig(enable_text=True, enable_audio=False, enable_visual=False)
    output_handler = DecisionOutput(config=config)
    
    # Create dismissal decision
    dismissal_decision = Decision(
        decision_id="dismissal_1",
        event_type=dismissal_event,
        confidence=0.90,
        timestamp=time.time(),
        trajectory=Trajectory(
            positions=[Position3D(x=0.0, y=1.0, z=-10.0)],
            timestamps=[1.0],
            velocities=[Vector3D(vx=0.0, vy=0.0, vz=20.0)],
            start_position=Position3D(x=0.0, y=1.0, z=-10.0)
        ),
        detections=[
            Detection(
                class_id=0,
                class_name="ball",
                bounding_box=BoundingBox(x=640.0, y=360.0, width=20.0, height=20.0),
                confidence=0.95
            )
        ],
        reasoning="Dismissal detected",
        video_references=[VideoReference(camera_id="cam1", frame_number=100, timestamp=1.0)],
        requires_review=False
    )
    
    # Create other decision
    other_decision = Decision(
        decision_id="other_1",
        event_type=other_event,
        confidence=0.85,
        timestamp=time.time(),
        trajectory=Trajectory(
            positions=[Position3D(x=0.0, y=1.0, z=-10.0)],
            timestamps=[1.0],
            velocities=[Vector3D(vx=0.0, vy=0.0, vz=20.0)],
            start_position=Position3D(x=0.0, y=1.0, z=-10.0)
        ),
        detections=[
            Detection(
                class_id=0,
                class_name="ball",
                bounding_box=BoundingBox(x=640.0, y=360.0, width=20.0, height=20.0),
                confidence=0.90
            )
        ],
        reasoning="Other event detected",
        video_references=[VideoReference(camera_id="cam1", frame_number=100, timestamp=1.0)],
        requires_review=False
    )
    
    # Get priorities
    dismissal_priority = output_handler.get_decision_priority(dismissal_decision)
    other_priority = output_handler.get_decision_priority(other_decision)
    
    # Property: Dismissal events SHALL have higher priority than other events
    assert dismissal_priority == DecisionPriority.HIGH, (
        f"Dismissal event {dismissal_event} should have HIGH priority, got {dismissal_priority}"
    )
    
    assert other_priority.value < dismissal_priority.value, (
        f"Dismissal priority ({dismissal_priority.value}) should be higher than "
        f"other event priority ({other_priority.value})"
    )
    
    # Verify priority values
    assert dismissal_priority.value == 3, "HIGH priority should be 3"
    assert other_priority.value <= 2, "Non-dismissal priority should be <= 2"


