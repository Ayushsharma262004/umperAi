# Property 23: Confidence Score Bounds
# ============================================================================

@given(
    trajectory=trajectory_strategy(min_length=5, max_length=20),
    num_detections=st.integers(min_value=1, max_value=10)
)
def test_property_23_confidence_score_bounds(trajectory, num_detections):
    """
    Property 23: Confidence Score Bounds
    
    For any decision generated by the Decision Engine, the confidence score
    SHALL be in the range [0, 100] percent (or [0.0, 1.0] as a fraction).
    
    Validates: Requirements 11.1
    """
    engine = DecisionEngine()
    calibration = CalibrationData(
        calibration_name="test_calibration",
        created_at="2024-01-01T00:00:00Z",
        pitch_boundary=[
            Point2D(x=100.0, y=100.0),
            Point2D(x=1180.0, y=100.0),
            Point2D(x=1180.0, y=620.0),
            Point2D(x=100.0, y=620.0)
        ],
        crease_lines={
            "bowling": [Point2D(x=640.0, y=-10.0), Point2D(x=640.0, y=-10.0)],
            "batting": [Point2D(x=640.0, y=0.0), Point2D(x=640.0, y=0.0)]
        },
        wide_guidelines={"left": -1.0, "right": 1.0},
        stump_positions={
            "bowling": Point2D(x=640.0, y=-10.0),
            "batting": Point2D(x=640.0, y=0.0)
        },
        camera_calibrations={}
    )
    
    # Generate random detections
    detections = []
    for _ in range(num_detections):
        confidence = np.random.uniform(0.0, 1.0)
        class_id = np.random.randint(0, 8)
        detections.append(Detection(
            class_id=class_id,
            class_name=f"class_{class_id}",
            bounding_box=BoundingBox(
                x=np.random.uniform(0.0, 1280.0),
                y=np.random.uniform(0.0, 720.0),
                width=np.random.uniform(10.0, 200.0),
                height=np.random.uniform(10.0, 200.0)
            ),
            confidence=confidence
        ))
    
    # Classify delivery
    decision = engine.classify_delivery(trajectory, detections, calibration)
    
    # Property: Confidence SHALL be in range [0.0, 1.0]
    assert 0.0 <= decision.confidence <= 1.0, (
        f"Confidence score out of bounds: {decision.confidence}. "
        f"Expected range [0.0, 1.0]"
    )


# ============================================================================
# Property 24: Low Confidence Flagging
# ============================================================================

@settings(suppress_health_check=[HealthCheck.too_slow])
@given(
    trajectory=trajectory_strategy(min_length=5, max_length=20),
    low_confidence=st.floats(min_value=0.0, max_value=0.79)
)
def test_property_24_low_confidence_flagging(trajectory, low_confidence):
    """
    Property 24: Low Confidence Flagging
    
    For any decision with confidence score below 80%, the Decision Engine
    SHALL flag the decision for manual review and log it for post-match analysis.
    
    Validates: Requirements 11.2, 11.4
    """
    engine = DecisionEngine()
    calibration = CalibrationData(
        calibration_name="test_calibration",
        created_at="2024-01-01T00:00:00Z",
        pitch_boundary=[
            Point2D(x=100.0, y=100.0),
            Point2D(x=1180.0, y=100.0),
            Point2D(x=1180.0, y=620.0),
            Point2D(x=100.0, y=620.0)
        ],
        crease_lines={
            "bowling": [Point2D(x=640.0, y=-10.0), Point2D(x=640.0, y=-10.0)],
            "batting": [Point2D(x=640.0, y=0.0), Point2D(x=640.0, y=0.0)]
        },
        wide_guidelines={"left": -1.0, "right": 1.0},
        stump_positions={
            "bowling": Point2D(x=640.0, y=-10.0),
            "batting": Point2D(x=640.0, y=0.0)
        },
        camera_calibrations={}
    )
    
    # Create detections with low confidence
    detections = [
        Detection(
            class_id=0,
            class_name="ball",
            bounding_box=BoundingBox(x=640.0, y=360.0, width=20.0, height=20.0),
            confidence=low_confidence
        )
    ]
    
    # Classify delivery
    decision = engine.classify_delivery(trajectory, detections, calibration)
    
    # Property: If confidence < 80%, requires_review SHALL be True
    if decision.confidence < 0.80:
        assert decision.requires_review is True, (
            f"Decision with confidence {decision.confidence:.2f} (<80%) "
            f"should be flagged for review, but requires_review={decision.requires_review}"
        )


