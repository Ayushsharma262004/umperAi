def test_decision_engine_config_validation():
    """Test DecisionEngineConfig validation."""
    # Valid config
    config = DecisionEngineConfig(
        min_confidence_threshold=0.70,
        review_threshold=0.80
    )
    assert config.min_confidence_threshold == 0.70
    
    # Invalid min_confidence_threshold (out of range)
